/**
 * Code generators for React wrappers using @lit/react's createComponent().
 */
import type { ComponentMeta } from './types.js';

/** Core package name — React wrappers import from this package. */
const CORE_PKG = '@getufy/flint-ui';

// ─── helpers ────────────────────────────────────────────────────────────────

/**
 * Convert a source file path (e.g. 'src/button/flint-button.ts') to a
 * package sub-path import (e.g. 'flint-ui/button/flint-button').
 * Strips 'src/' prefix and '.ts' suffix.
 */
function packageImport(sourceFile: string): string {
    const stripped = sourceFile.replace(/^src\//, '').replace(/\.ts$/, '');
    return `${CORE_PKG}/${stripped}`;
}

// ─── event constants file ────────────────────────────────────────────────────

/**
 * Generate the content of `react/src/events/<tagname>.ts`.
 * Contains a const object mapping short keys → DOM event name strings.
 *
 * Example output:
 *   export const FlintSwitchEvents = {
 *     CHANGE: 'flint-switch-change',
 *   } as const;
 */
export function generateEventsFile(meta: ComponentMeta): string {
    if (meta.events.length === 0) return '';

    const constName = meta.className + 'Events';
    const entries = meta.events
        .map(e => `    ${e.constKey}: '${e.domName}',`)
        .join('\n');

    return (
        `// Auto-generated — do not edit\n` +
        `export const ${constName} = {\n${entries}\n} as const;\n`
    );
}

// ─── React wrapper component (using @lit/react) ────────────────────────────

/**
 * Generate the content of `react/src/components/<ClassName>.tsx`.
 *
 * Uses @lit/react's createComponent() which handles:
 * - Property syncing (no useEffect flicker)
 * - Event forwarding with proper typing
 * - Ref forwarding
 * - React 18 + 19 compatibility
 */
export function generateWrapper(
    meta: ComponentMeta,
    /** Absolute path to the components output dir */
    _outDir: string,
    /** Absolute path to the events output dir */
    _eventsDir: string,
): string {
    const { tagName, className, events, sourceFile } = meta;

    const srcImport = packageImport(sourceFile);
    const hasEvents = events.length > 0;

    const lines: string[] = [];

    lines.push(`// Auto-generated by scripts/generate-react-wrappers.ts — do not edit`);
    lines.push(`// Source: ${sourceFile}`);
    lines.push('');
    lines.push(`import React from 'react';`);
    lines.push(`import { createComponent${hasEvents ? ', type EventName' : ''} } from '@lit/react';`);
    lines.push(`import { ${className} as ${className}Element } from '${srcImport}';`);
    lines.push('');

    // Build the createComponent call
    lines.push(`export const ${className} = createComponent({`);
    lines.push(`    tagName: '${tagName}',`);
    lines.push(`    elementClass: ${className}Element,`);
    lines.push(`    react: React,`);

    if (hasEvents) {
        lines.push(`    events: {`);
        for (const e of events) {
            lines.push(`        ${e.reactProp}: '${e.domName}' as EventName<CustomEvent>,`);
        }
        lines.push(`    },`);
    }

    lines.push(`});`);
    lines.push('');
    lines.push(`export type ${className}Props = React.ComponentProps<typeof ${className}>;`);
    lines.push('');

    return lines.join('\n');
}

// ─── barrel index ────────────────────────────────────────────────────────────

/** Generate `react/src/index.ts` re-exporting all components and events. */
export function generateIndex(components: ComponentMeta[]): string {
    const lines: string[] = [
        `// Auto-generated by scripts/generate-react-wrappers.ts — do not edit`,
        '',
    ];
    // De-duplicate class names (multiple files could share a class name in edge cases)
    const seen = new Set<string>();
    for (const c of components) {
        if (seen.has(c.className)) continue;
        seen.add(c.className);
        lines.push(`export { ${c.className}, type ${c.className}Props } from './components/${c.className}.js';`);
    }
    lines.push('');
    lines.push(`export * from './events/index.js';`);
    lines.push('');
    return lines.join('\n');
}

/** Generate `react/src/events/index.ts` re-exporting all event constants. */
export function generateEventsIndex(components: ComponentMeta[]): string {
    const lines: string[] = [
        `// Auto-generated by scripts/generate-react-wrappers.ts — do not edit`,
        '',
    ];
    const seen = new Set<string>();
    for (const c of components) {
        if (c.events.length === 0) continue;
        if (seen.has(c.tagName)) continue;
        seen.add(c.tagName);
        lines.push(`export { ${c.className}Events } from './${c.tagName}.js';`);
    }
    lines.push('');
    return lines.join('\n');
}

// ─── per-component barrels ──────────────────────────────────────────────────

/**
 * Extract the component directory name from a sourceFile path.
 * e.g. 'src/button/flint-button.ts' → 'button'
 */
function componentDir(sourceFile: string): string {
    const parts = sourceFile.replace(/^src\//, '').split('/');
    return parts[0];
}

/**
 * Group components by their source directory.
 * Returns a Map of directory name → ComponentMeta[].
 */
export function groupByDirectory(components: ComponentMeta[]): Map<string, ComponentMeta[]> {
    const groups = new Map<string, ComponentMeta[]>();
    for (const c of components) {
        const dir = componentDir(c.sourceFile);
        if (!groups.has(dir)) groups.set(dir, []);
        groups.get(dir)!.push(c);
    }
    return groups;
}

/**
 * Generate a per-component barrel file (e.g. `react/src/button.ts`)
 * that re-exports all wrappers from that component directory.
 */
export function generateComponentBarrel(components: ComponentMeta[]): string {
    const lines: string[] = [
        `// Auto-generated by scripts/generate-react-wrappers.ts — do not edit`,
        '',
    ];
    const seen = new Set<string>();
    for (const c of components) {
        if (seen.has(c.className)) continue;
        seen.add(c.className);
        lines.push(`export { ${c.className}, type ${c.className}Props } from './components/${c.className}.js';`);
    }
    // Also re-export any events from these components
    for (const c of components) {
        if (c.events.length === 0) continue;
        if (seen.has(c.tagName + '_events')) continue;
        seen.add(c.tagName + '_events');
        lines.push(`export { ${c.className}Events } from './events/${c.tagName}.js';`);
    }
    lines.push('');
    return lines.join('\n');
}

/**
 * Generate the `exports` map for the React package.json.
 * Includes the root '.' export and per-component exports.
 */
export function generateExportsMap(components: ComponentMeta[]): Record<string, { types: string; import: string }> {
    const exports: Record<string, { types: string; import: string }> = {
        '.': {
            types: './dist/index.d.ts',
            import: './dist/index.js',
        },
    };

    const groups = groupByDirectory(components);
    for (const dir of [...groups.keys()].sort()) {
        exports[`./${dir}`] = {
            types: `./dist/${dir}.d.ts`,
            import: `./dist/${dir}.js`,
        };
    }

    return exports;
}

/**
 * Generate `react/src/custom-elements.d.ts`.
 * Augments the JSX namespace so TypeScript accepts custom-element tags.
 * Uses module augmentation for React 19 compatibility.
 */
export function generateJsxDeclarations(components: ComponentMeta[]): string {
    const lines: string[] = [
        `// Auto-generated by scripts/generate-react-wrappers.ts — do not edit`,
        '',
        `import 'react';`,
        '',
        `declare module 'react' {`,
        `    namespace JSX {`,
        `        interface IntrinsicElements {`,
    ];

    const seen = new Set<string>();
    for (const c of components) {
        if (seen.has(c.tagName)) continue;
        seen.add(c.tagName);
        lines.push(
            `            '${c.tagName}': React.DetailedHTMLProps<React.HTMLAttributes<HTMLElement>, HTMLElement>;`,
        );
    }

    lines.push(`        }`);
    lines.push(`    }`);
    lines.push(`}`);
    lines.push('');
    return lines.join('\n');
}
